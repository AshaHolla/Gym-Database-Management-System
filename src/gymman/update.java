    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int checkid=0;
        String id = jTextField8.getText();
        try
        {
           Connection con = connectionprovider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where member_id ='"+id+"'");
            while(rs.next())
            {
                checkid=1;
                jTextField8.setEditable(false);
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(6));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(rs.getString(9));
                jTextField6.setText(rs.getString(7));
                jTextField7.setText(rs.getString(10));
                jTextField9.setText(rs.getString(4));
                jTextField10.setText(rs.getString(11));
                jTextField11.setText(rs.getString(8));
                jTextField9.setEditable(false);
                jTextField10.setEditable(false);
                jTextField11.setEditable(false);
               
                
                
               
                
           }
            if(checkid==0)
            {
                JOptionPane.showMessageDialog(null,"member id does not exist");
            }
            
             
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

